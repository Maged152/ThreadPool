/**
 * @brief Multiplies two integers.
 * 
 * @param in0 The first integer input.
 * @param in1 The second integer input.
 * @return product of the two integers. 
 */
int mul(const int in0, const int in1)
{
    return in0 * in1;
}

/**
 * @brief Subtract two integers.
 * 
 * @param in0 The first integer input.
 * @param in1 The second integer input.
 * @return The result of the subtraction. 
 */
int sub(const int in0, const int in1)
{
    return in0 - in1;
}
